//Imprimir diez veces la serie de numeros del 1 al 10.
public class Ejercicio16 {

	public static void main(String[] args) {
		
		
		for (int i=0;i<10;i++) {
			for(int j=1;j<=10;j++) {
				
				System.out.print(j+" ");
				
			}
			System.out.println();
		}
		

	}

}
