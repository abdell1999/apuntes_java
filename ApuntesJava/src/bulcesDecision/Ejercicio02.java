package bulcesDecision;



//Hacer un codigo que imprima los numeros del 100 al 1, es decir en orden decreciente
public class Ejercicio02 {

	public static void main(String[] args) {
		
		
	int numero = 100;
		
		while (numero>=1) {
			
			System.out.println(numero);
			numero--;
		}
		
		

	}

}
